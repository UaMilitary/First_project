import re



def find_all_phones(text):
    pattern1 = r"\+\d{3}\(\d{2}\)\d{3}-\d{2}-\d{2}"
    pattern2 = r"\+\d{3}\(\d{2}\)\d{3}-\d{1}-\d{3}"
    result1 = re.findall(pattern1, text)
    result2 = re.findall(pattern2, text)
    a = result2 + result1
    return a

